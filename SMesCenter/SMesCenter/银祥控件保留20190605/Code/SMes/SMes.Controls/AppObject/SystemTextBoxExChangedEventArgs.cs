using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace SMes.Controls.AppObject
{
    public class SystemTextBoxExChangedEventArgs : EventArgs
    {
        List<string> _value;
        public SystemTextBoxExChangedEventArgs(List<string> value)
        {
            _value = value;
        }

        public List<string> LovReturnValue
        {
            get
            {
                return _value;
            }
            set
            {
                _value = value;
            }
        }
    }
}
