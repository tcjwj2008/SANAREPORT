using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace SMesCenter.AppObj
{
    public class SystemInfo
    {
        string _systemName = "";
        string _systemVersion = "";

        public string SystemName
        {
            get
            {
                return _systemName;
            }
            set
            {
                _systemName = value;
            }
        }

        public string SystemVersion
        {
            get
            {
                return _systemVersion;
            }
            set
            {
                _systemVersion = value;
            }
        }
    }
}
